package com.know.interfaces;

/**
 * Since static methods are supported in interface from Java version 1.8 
 * a public static void main(string[] arg) is also allowed , hence we can
 * RUN an Interface
 * 
 * @author KnowGroup
 */
public interface Lesson_06_Run_Interface_by_Main_Method {

    public static void main(String[] arg){
        System.out.println("I am interface");
    }
}
